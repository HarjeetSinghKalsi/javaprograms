package basic;

public class Math_Class {

	public static void main(String[] args)
	{
		System.out.println(Math.addExact(95, 59));
		System.out.println(Math.subtractExact(100, 34));
		System.out.println(Math.multiplyExact(334, 356));
		System.out.println(Math.min(4.56, 40.144));
		System.out.println(Math.max(45.5, 2.687));
		System.out.println(Math.abs(-15.678)); //convert the negative value into positive value
		System.out.println(Math.random()); //Math.random can generate any random number b/w 0 to 1 of double data type.
	}

}
 