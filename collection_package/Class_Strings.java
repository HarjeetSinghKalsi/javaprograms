package collections;
public class Class_Strings {

	public static void main(String[] args) 
	{
String oldstring=new String("Manish");
	
	oldstring.concat("Tiwari");
		System.out.println(oldstring);
		//Immutable

		StringBuilder s1=new StringBuilder("Harjeet");
			
			s1.append(" kalsi");
			System.out.println(s1);
		//Mutable
StringBuffer s2=new StringBuffer("Ajit");
			
			s2.append(" Choudhary");
			System.out.println(s2);
			//Mutable

		
	}

}
